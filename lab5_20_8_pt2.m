% 1) H(z) = z/(z - 0.5)  =>  H(z) = 1/(1 - 0.5 z^-1)
num3 = [1 0];
den3 = [1 -0.5];

% 2) H(z) = 1 - 0.5 z^-1
num4 = [1 -0.5];
den4 = [1];

% 3) H(z) = (0.5 z^2 - 0.32)/(z^2 - 0.5 z + 0.25)
% (z^-1 form is (0.5 - 0.32 z^-2)/(1 - 0.5 z^-1 + 0.25 z^-2))
num5 = [0.5 0 -0.32];
den5 = [1 -0.5 0.25];

% 4) H(z) = (1 - 0.9 z^-1 + 0.81 z^-2)/(1 - 0.6 z^-1 + 0.36 z^-2)
num6 = [1 -0.9 0.81];
den6 = [1 -0.6 0.36];

figure; 
subplot(2,2,1);
zplane(num3, den3);
title('H_1(z) = z/(z - 0.5)');

subplot(2,2,2);
zplane(num4, den4);
title('H_2(z) = 1 - 0.5 z^{-1}');

subplot(2,2,3);
zplane(num5, den5);
title('H_3(z) = (0.5 z^2 - 0.32)/(z^2 - 0.5 z + 0.25)');

subplot(2,2,4);
zplane(num6, den6);
title('H_4(z) = (1 - 0.9 z^{-1} + 0.81 z^{-2})/(1 - 0.6 z^{-1} + 0.36 z^{-2})');
